    private TaiSanPhongMay getTaiSanPhongMaybyForm() throws SQLException {
        String tenTaiSan = (String) CB_TaiSan.getSelectedItem();
        String iDTaiSan = "";
        
        for (Map.Entry<String, String> entry : taiSanHashMap.entrySet()) {
            if (entry.getValue().equals(tenTaiSan)) {
                iDTaiSan = entry.getKey();
                break;
            }
        }
        
        String tenPhongMay = (String) CB_PhongMay.getSelectedItem();
        String iDPhongMay = "";
        
        for (Map.Entry<String, String> entry : phongMayHashMap.entrySet()) {
            if (entry.getValue().equals(tenPhongMay)) {
                iDPhongMay = entry.getKey();
                break;
            }
        }
        
        return getTaiSanPhongMaybyiDTaiSan_iDPhongMay(iDTaiSan, iDPhongMay);
    }
    
    // Thịnh thêm get bằng iDTaiSan và iDPhongMay
    public TaiSanPhongMay getTaiSanPhongMaybyiDTaiSan_iDPhongMay(String iDtaiSan, String iDPhongMay) throws SQLException {
        TaiSanPhongMay taiSanPhongMay = new TaiSanPhongMay();
        Connection conn = Database.DatabaseHelper.getDBConnection();
        Statement sttm = conn.createStatement();
        String query = "select * from TaiSanPhongMay where iDTaiSan =" + "'" + iDtaiSan + "'" + "and iDPhongMay = " + "'" + iDPhongMay +"'";
        ResultSet result = sttm.executeQuery(query);
        
        while(result.next()) {
            taiSanPhongMay.setiD(result.getString(1));
            taiSanPhongMay.setiDTaiSan(result.getString(2));
            taiSanPhongMay.setiDPhongMay(result.getString(3));
            taiSanPhongMay.setSoLuong(result.getInt(4));
        }
        
        return taiSanPhongMay;
    }
    

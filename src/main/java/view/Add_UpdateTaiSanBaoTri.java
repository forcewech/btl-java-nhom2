    private void hienThiComboBoxForPhongMay() {
        PhongMayDAO phongMayDAO = new PhongMayDAO();
        ArrayList<PhongMay> phongMaysList = (ArrayList < PhongMay >)phongMayDAO.getAll();
        
        phongMayHashMap = new HashMap<>();
        
        phongMaysList.forEach(phongMay -> {
            phongMayHashMap.put(phongMay.getMaPhong(), phongMay.getTenPhong());
        });
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(phongMayHashMap.values().toArray(new String[0]));
        CB_PhongMay.setModel(model);
    }
    
    private void hienThiComboBoxForTaiSan() {        
        TaiSanDAO taiSanDAO = new TaiSanDAO();
        ArrayList<TaiSan> taiSansList = (ArrayList<TaiSan>)taiSanDAO.getAll();
        
        taiSanHashMap = new HashMap<>();
        
        taiSansList.forEach(taiSan -> {
            taiSanHashMap.put(taiSan.getMaTaiSan(), taiSan.getTenTaiSan());
        });
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(taiSanHashMap.values().toArray(new String[0]));
        CB_TaiSan.setModel(model);
    }
    
    

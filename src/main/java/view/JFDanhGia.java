    public void updateTable() {
        DefaultTableModel dgModel = (DefaultTableModel) jTableDanhGia.getModel();
        dgModel.setRowCount(0);
        for (DanhGia dg : dgDAO.getAll()) {
            Object dataRow[] = new Object[2];
            dataRow[0] = dg.getMaPhieu();
            dataRow[1] = dg.getDanhGia();         
            dgModel.addRow(dataRow);
        }
    }
    
    DanhGiaDAO dgDAO = new DanhGiaDAO();
    
    public void fillTable(){
        updateTable();
    }
    
    public void setModel(DanhGia dg){     
        jTextFieldMaPhieu.setText(dg.getMaPhieu());
        jTextAreaDanhGia.setText(dg.getDanhGia());
    }
    
    public DanhGia getModel(){
        DanhGia dg = new DanhGia();
        dg.setMaPhieu(jTextFieldMaPhieu.getText().trim());
        dg.setDanhGia(jTextAreaDanhGia.getText().trim());
        return dg;
    }
    
    public void resetForm(){
        jTextFieldMaPhieu.setText("");
        jTextAreaDanhGia.setText("");
    }
    

    private void jButtonTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTimActionPerformed
        // TODO add your handling code here:
        jTextFieldMaPhieu.setEnabled(false);
        if(jTextFieldMaPhieu.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã phiếu");
        } else {
            DanhGia dg = dgDAO.findDGByID(jTextFieldMaPhieu.getText());
            if(dg != null)
                setModel(dg);
            else
                JOptionPane.showMessageDialog(this, "Không tìm thấy đánh giá");
        }
    }//GEN-LAST:event_jButtonTimActionPerformed


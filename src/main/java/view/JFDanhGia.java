    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
        DanhGia dg = getModel();
        if(dgDAO.add(dg) > 0){
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            updateTable();
        }      
    }//GEN-LAST:event_jButtonThemActionPerformed

    private void jButtonSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSuaActionPerformed
        // TODO add your handling code here:
        DanhGia dg = getModel();
        jTextFieldMaPhieu.setEnabled(false);
        if(dgDAO.update(dg) > 0)
        {
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            updateTable();
        }   
    }//GEN-LAST:event_jButtonSuaActionPerformed

    String maPhieuClick = "-1";
    private void jTableDanhGiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDanhGiaMouseClicked
        // TODO add your handling code here:
        jTextFieldMaPhieu.setEnabled(false);
        int position = jTableDanhGia.rowAtPoint(evt.getPoint());
        maPhieuClick = jTableDanhGia.getValueAt(position, 0).toString();
        DanhGia dg = dgDAO.findDGByID(maPhieuClick);
        if(dg != null)
            setModel(dg);
    }//GEN-LAST:event_jTableDanhGiaMouseClicked


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTextFieldID.setEnabled(false);
        if(jTextFieldID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã");
        } else {
            NhiemVuThayDoiTaiSan nvtdts = nvtdtsDAO.findNVTDTSByID(jTextFieldID.getText());
            if(nvtdts != null)
                setModel(nvtdts);
            else
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhiệm vụ!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jTextFieldID.setEnabled(false);
        NhiemVuThayDoiTaiSan nvtdts = getModel();
        if(nvtdtsDAO.add(nvtdts) > 0){
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            fillTable();
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        NhiemVuThayDoiTaiSan nvtdts = getModel();
        jTextFieldID.setEnabled(false);
        if(nvtdtsDAO.update(nvtdts) > 0){
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            fillTable();
        }         
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jTextFieldID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã");
        } else {
            int result = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_NO_CANCEL_OPTION);    
            if (result == JOptionPane.YES_OPTION) {
                if(nvtdtsDAO.delete(jTextFieldID.getText()) > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công!");
                    fillTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy nhiệm vụ!");
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


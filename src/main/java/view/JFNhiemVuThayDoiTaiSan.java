    private void jTableNhiemVuThayDoiTaiSanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNhiemVuThayDoiTaiSanMouseClicked
        // TODO add your handling code here:
        jTextFieldID.setEnabled(false);
        int position = jTableNhiemVuThayDoiTaiSan.rowAtPoint(evt.getPoint());
        idClick = jTableNhiemVuThayDoiTaiSan.getValueAt(position, 0).toString();
        NhiemVuThayDoiTaiSan nvtdts = nvtdtsDAO.findNVTDTSByID(idClick);
        if(nvtdts != null)
            setModel(nvtdts);
    }//GEN-LAST:event_jTableNhiemVuThayDoiTaiSanMouseClicked

    

    private void jButtonXemTaiSanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXemTaiSanActionPerformed
        // TODO add your handling code here:
        if(jTextFieldPhong.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập phòng!");
        } else {
            List<TaiSanPhongMay> ls = new ArrayList<>();
            ls = tspmDAO.getTaiSanPhongMayByiDPhongMay(jTextFieldPhong.getText());
            if(ls != null)
                fillTableTaiSanPhongMay(ls);
            else
                JOptionPane.showMessageDialog(this, "Không tìm thấy phòng!");
        }
    }//GEN-LAST:event_jButtonXemTaiSanActionPerformed


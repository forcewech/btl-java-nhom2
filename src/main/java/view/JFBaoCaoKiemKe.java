    public void fillTableTaiSanPhongMay(List<TaiSanPhongMay> ls){
        DefaultTableModel tspmModel = (DefaultTableModel) jTableTaiSanPhong.getModel();
        tspmModel.setRowCount(0);     
        for(int i = 0; i < ls.size(); i++){
            Object dataRow[] = new Object[3];
            TaiSanPhongMay tspm = ls.get(i);
            TaiSan ts = tsDAO.findTSByID(tspm.getiDTaiSan());
            dataRow[0] = tspm.getiDTaiSan();
            dataRow[1] = ts.getTenTaiSan();
            dataRow[2] = tspm.getSoLuong();  
            tspmModel.addRow(dataRow);
        }    
    }
    
